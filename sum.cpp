//Wap to addition of two number from user input
// << -->insertion operator and >> -->this is extraction operator
#include<iostream>
using namespace std;
int main()
{
	int a,b,c;
	cout<<"Enter the value of a and b:";
	cin>>a>>b;//read a and read b
	c=a+b;
	cout<<"Sum is: "<<c <<endl;
	return 0;
}
